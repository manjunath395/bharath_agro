<script>
export default {
  data() {
    return {
      mySwiper: null,
    };
  },
  mounted() {
    // this.initSwiper();
    this.initSwiper();

  const paginationSwitches = document.querySelectorAll('.swiper-pagination-custom li');
  paginationSwitches.forEach((switchElement, index) => {
    switchElement.addEventListener('click', () => {
      this.handlePaginationSwitchClick(index);
    });
  });
  },
  methods: {
    initSwiper() {
      this.mySwiper = new Swiper(".swiper-container", {
        autoHeight: true,
        // autoplay: {
        //   delay: 5000,
        //   disableOnInteraction: false,
        // },
        autoplay: false,
        speed: 500,
        direction: "horizontal",
        navigation: {
          nextEl: ".swiper-button-next",
          prevEl: ".swiper-button-prev",
        },
        pagination: {
          el: ".swiper-pagination",
        //   type: "progressbar",
        },
        loop: false,
        effect: "slide",
        spaceBetween: 80,
        on: {
          init: () => {
            this.updatePaginationSwitch(0);
          },
          slideChangeTransitionStart: () => {
            this.updatePaginationSwitch(this.mySwiper.realIndex);
          },
        },
      });
    },
    updatePaginationSwitch(index) {
      const paginationSwitches = document.querySelectorAll(".swiper-pagination-switch");
      paginationSwitches.forEach((switchElement) => {
        switchElement.classList.remove("active");
      });
      paginationSwitches[index].classList.add("active");
    },
    handlePaginationSwitchClick(index) {
         console.log("Clicked on marker at index:", index);
      this.mySwiper.slideTo(index);
      this.updatePaginationSwitch(index);
    },
  },
};
</script>

